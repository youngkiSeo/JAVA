package com.green.java.ch04;

import java.util.Scanner;

public class FlowEx23 {
    public static void main(String[] args) {
        // 변수 i를 활용하여 4- I can do it.

        Scanner scan = new Scanner(System.in);
        int N = scan.nextInt();
        while (N-- > 0) {
            System.out.println(N + "- I can do it.");


        }
    }
}
